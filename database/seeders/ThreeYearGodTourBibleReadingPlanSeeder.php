/**
 * Seeds the "Every Word in the Bible / 3-Year God Tour" plan, 1120 daily readings (one chapter a day, with short chapters occasionally combined).
 *
 * Idempotent: re-running updates plan metadata, deletes existing days and
 * fragments via cascade, and rebuilds them.
