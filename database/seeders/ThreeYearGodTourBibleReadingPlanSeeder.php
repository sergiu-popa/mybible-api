{
    use ParsesPlanReferences;

    private const SLUG = '3-year-god-tour-reading-plan';

    public function run(): void
    {
        DB::transaction(function (): void {
            $plan = ReadingPlan::query()->updateOrCreate(
                ['slug' => self::SLUG],
                [
                    'name' => [
                        'en' => 'Every Word in the Bible — 3-Year Plan',
                        'ro' => 'Fiecare cuvânt din Biblie — plan de 3 ani',
                    ],
                    'description' => [
                        'en' => 'Read every word of the Bible at a manageable pace of one chapter a day, alternating between the Old and New Testaments to add variety. Some short chapters are combined, and the plan works out to a three-year total.',
                        'ro' => 'Citește fiecare cuvânt al Bibliei într-un ritm accesibil — un capitol pe zi, alternând între Vechiul și Noul Testament pentru varietate. Unele capitole scurte sunt combinate, iar planul se încheie în trei ani.',
                    ],
                    'image' => [
                        'en' => 'https://placehold.co/1200x630?text=3-Year+God+Tour',
                        'ro' => 'https://placehold.co/1200x630?text=Plan+3+ani',
                    ],
                    'thumbnail' => [
                        'en' => 'https://placehold.co/400x400?text=3+Years',
                        'ro' => 'https://placehold.co/400x400?text=3+ani',
                    ],
                    'status' => ReadingPlanStatus::Published,
                    'published_at' => now(),
                ],
            );

            $plan->days()->delete();

            foreach (self::DAYS as $position => $references) {
                $day = ReadingPlanDay::query()->create([
                    'reading_plan_id' => $plan->id,
                    'position' => $position,
                ]);

                $this->seedReferenceFragments($day, $references);
            }
        });
    }

