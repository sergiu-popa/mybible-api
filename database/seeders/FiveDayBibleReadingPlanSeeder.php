                [
                    'name' => [
                        'en' => '5 Day Bible Reading Program',
                        'ro' => 'Plan de citire a Bibliei în 5 zile pe săptămână',
                    ],
                    'description' => [
                        'en' => 'Read the entire Bible chronologically over a year, five days a week. The two-day weekend buffer keeps the schedule realistic, and even the Psalms are placed in chronological order to help the Bible\'s story make sense.',
