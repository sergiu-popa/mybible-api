                    'image' => [
                        'en' => 'https://placehold.co/1200x630?text=5+Day+Bible+Plan',
                        'ro' => 'https://placehold.co/1200x630?text=Plan+5+zile',
                    ],
                    'thumbnail' => [
                        'en' => 'https://placehold.co/400x400?text=5+Days',
                        'ro' => 'https://placehold.co/400x400?text=5+zile',
                    ],
                    'status' => ReadingPlanStatus::Published,
                    'published_at' => now(),
                ],
            );

            $plan->days()->delete();

            foreach (self::DAYS as $position => $references) {
                $day = ReadingPlanDay::query()->create([
                    'reading_plan_id' => $plan->id,
                    'position' => $position,
                ]);

                $this->seedReferenceFragments($day, $references);
            }
        });
    }

