    private const SLUG = 'discipleship-journal-bible-reading-plan';

    public function run(): void
    {
        DB::transaction(function (): void {
            $plan = ReadingPlan::query()->updateOrCreate(
                ['slug' => self::SLUG],
                [
                    'name' => [
                        'en' => 'The Discipleship Journal Bible Reading Plan',
                        'ro' => 'Plan de citire a Bibliei Discipleship Journal',
                    ],
                    'description' => [
                        'en' => 'Read the entire Bible in a year through four daily readings drawn from the New Testament, the Psalms, and the Old Testament. Each month covers 25 readings, leaving five days for catch-up or reflection.',
