                    ],
                    'image' => [
                        'en' => 'https://placehold.co/1200x630?text=Discipleship+Journal',
                        'ro' => 'https://placehold.co/1200x630?text=Discipleship+Journal',
                    ],
                    'thumbnail' => [
                        'en' => 'https://placehold.co/400x400?text=DJ+Plan',
                        'ro' => 'https://placehold.co/400x400?text=DJ+Plan',
                    ],
                    'status' => ReadingPlanStatus::Published,
                    'published_at' => now(),
                ],
            );

            $plan->days()->delete();

            foreach (self::DAYS as $position => $references) {
                $day = ReadingPlanDay::query()->create([
                    'reading_plan_id' => $plan->id,
                    'position' => $position,
                ]);

                $this->seedReferenceFragments($day, $references);
            }
        });
    }

